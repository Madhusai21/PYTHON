num1=int(input("enter the first number:"))
num2=int(input("enter the second number:"))
op=input("enter the mathematical operator")
if op=='+':
       print("result is:",num1+num2)
if op=='-':
       print("result is:",num1-num2)
if op=='/':
       print("result is:",num1/num2)
if op=='%':
       print("result is:",num1%num2)
if op=='*':
       print("result is:",num1*num2)
if op=='**':
       print("result is:",num1**num2)
if op=='//':
       print("result is:",num1//num2)
       
